package br.com.hackforchange.bean;

public class EscalaEBIA {

	private int idEscalaEbia;
	private int ebiaUm;
	private int ebiaDois;
	private int ebiaTres;
	private int ebiaQuatro;
	private int ebiaCinco;
	private int ebiaSeis;
	private int ebiaSete;
	
	public EscalaEBIA() {
		
	}

	public EscalaEBIA(int idEscalaEbia, int ebiaUm, int ebiaDois, int ebiaTres, int ebiaQuatro, int ebiaCinco,
			int ebiaSeis, int ebiaSete) {
		this.idEscalaEbia = idEscalaEbia;
		this.ebiaUm = ebiaUm;
		this.ebiaDois = ebiaDois;
		this.ebiaTres = ebiaTres;
		this.ebiaQuatro = ebiaQuatro;
		this.ebiaCinco = ebiaCinco;
		this.ebiaSeis = ebiaSeis;
		this.ebiaSete = ebiaSete;
	}

	public int getIdEscalaEbia() {
		return idEscalaEbia;
	}

	public void setIdEscalaEbia(int idEscalaEbia) {
		this.idEscalaEbia = idEscalaEbia;
	}

	public int getEbiaUm() {
		return ebiaUm;
	}

	public void setEbiaUm(int ebiaUm) {
		this.ebiaUm = ebiaUm;
	}

	public int getEbiaDois() {
		return ebiaDois;
	}

	public void setEbiaDois(int ebiaDois) {
		this.ebiaDois = ebiaDois;
	}

	public int getEbiaTres() {
		return ebiaTres;
	}

	public void setEbiaTres(int ebiaTres) {
		this.ebiaTres = ebiaTres;
	}

	public int getEbiaQuatro() {
		return ebiaQuatro;
	}

	public void setEbiaQuatro(int ebiaQuatro) {
		this.ebiaQuatro = ebiaQuatro;
	}

	public int getEbiaCinco() {
		return ebiaCinco;
	}

	public void setEbiaCinco(int ebiaCinco) {
		this.ebiaCinco = ebiaCinco;
	}

	public int getEbiaSeis() {
		return ebiaSeis;
	}

	public void setEbiaSeis(int ebiaSeis) {
		this.ebiaSeis = ebiaSeis;
	}

	public int getEbiaSete() {
		return ebiaSete;
	}

	public void setEbiaSete(int ebiaSete) {
		this.ebiaSete = ebiaSete;
	}
	
}
